int		ft_sqrt(int nb)
{
	int x;
	int nx;

	x = 1;
	while (1)
	{
		nx = (x + nb / x) >> 1;
		if (x == nx && x * x == nb)
			return (x);
		if (nb <= 0 || nb > 2147483647 || (x == nx && x * x != nb))
			return (0);
		x = nx;
	}
}
